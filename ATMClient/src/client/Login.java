    public static boolean isNumeric(String cardNo, String pin) {
        if (cardNo.matches("-?\\d+(\\.\\d+)?")) {
            if (pin.length() == 6) {
                return pin.matches("-?\\d+(\\.\\d+)?");
            } else {
                return false;
            }
        }
        return false;
    }

    private void reset() {
        tfnumAcc.setText("");
        jpPin.setText("");
    }
    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed

        if (isNumeric(tfnumAcc.getText(), jpPin.getText())) {
            try {
                userInfo = client.getBank().login(tfnumAcc.getText(), jpPin.getText());
                if (userInfo == null) {
                    JOptionPane.showMessageDialog(null, "Sai số tài khoản hoặc mã PIN!", "Warning",
                            JOptionPane.WARNING_MESSAGE);
                  //  reset();
                } else if (userInfo.get(0).compareToIgnoreCase("logined") == 0) {
                    JOptionPane.showMessageDialog(null, "Bạn chưa kết thúc giao dịch!", "Warning",
                            JOptionPane.WARNING_MESSAGE);
                    reset();
                } else if (userInfo.get(0).compareToIgnoreCase("locked") == 0) {
                    JOptionPane.showMessageDialog(null, "Tài khoản của bạn đã bị khóa!", "Warning",
                            JOptionPane.WARNING_MESSAGE);
                    reset();
                } else {
                    //  System.out.println(userInfo);
                    Thread t = new Thread() {
                        @Override
                        public void run() {
                            try {
                                //while(true){
                                new GUI().setVisible(true);
                                dispose();
                                try {
                                    //10 minutes
                                    Thread.sleep(60000 * 10);
                                } catch (InterruptedException ex) {
                                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                                }
                                client.getBank().changeStatus(userInfo.get(0));
                                System.exit(0);
                            } catch (RemoteException ex) {
                                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }

                    };
                    t.start();
                }
            } catch (RemoteException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Số tài khoản và mã PIN phải là số và chiều dài mã PIN là 6!", "Warning",
                    JOptionPane.WARNING_MESSAGE);
            //reset();

        }

        //                JOptionPane.showMessageDialog(null, "Tài khoản của bạn đã bị khóa, xin liên lạc với ngân hàng gần nhất!", "Warning",
        //          JOptionPane.WARNING_MESSAGE);
        // System.exit(1);

    }//GEN-LAST:event_btLoginActionPerformed

